import {
  existsSync,
  mkdtempSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { dirname, isAbsolute, join, relative } from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

type QueryBlock = {
  name: string;
  sql: string;
};

type SourceMtTimes = {
  queries: number;
  migrations: number;
  typesql: number;
};

type TargetMtTimes = {
  queriesTs: number;
  schemaSql: number;
};

type TypeSqlConfig = {
  databaseUri: string;
  sqlDir: string;
  outDir?: string;
  client: string;
  moduleExtension?: string;
};

const scriptDir = dirname(fileURLToPath(import.meta.url));
const appDir = dirname(scriptDir);
const require = createRequire(import.meta.url);
const sqlDir = join(appDir, "sql");
const queriesSqlPath = join(sqlDir, "queries.sql");
const queriesTsPath = join(sqlDir, "queries.ts");
const schemaSqlPath = join(sqlDir, "schema.sql");
const migrationsDir = join(sqlDir, "migrations");
const typesqlConfigPath = join(appDir, "typesql.json");
const localDbPath = join(appDir, ".local.db");
const queryMarker = /^--\s*@query\s+([a-z][A-Za-z0-9]*)\s*$/;
const generatedHeader =
  "// Auto-generated by scripts/generate-queries.ts from sql/queries.sql. Do not edit manually.";
const generatedSchemaHeader =
  "-- Auto-generated by scripts/generate-queries.ts from sql/migrations. Do not edit manually.";

const args = new Set(process.argv.slice(2));
const watchMode = args.has("--watch");
const forceMode = args.has("--force") || (!watchMode && !args.has("--if-stale"));

function listSqlFiles(dir: string): string[] {
  const files: string[] = [];

  try {
    for (const entry of readdirSync(dir, { withFileTypes: true })) {
      const path = join(dir, entry.name);
      if (entry.isDirectory()) files.push(...listSqlFiles(path));
      if (entry.isFile() && entry.name.endsWith(".sql")) files.push(path);
    }
  } catch {
    return [];
  }

  return files.sort();
}

function mtime(path: string): number {
  return statSync(path).mtimeMs;
}

function getSourceMtTimes(): SourceMtTimes {
  const migrationFiles = listSqlFiles(migrationsDir);
  const migrations = migrationFiles.length === 0 ? 0 : Math.max(...migrationFiles.map(mtime));

  return {
    queries: mtime(queriesSqlPath),
    migrations,
    typesql: mtime(typesqlConfigPath),
  };
}

function getTargetMtTimes(): TargetMtTimes {
  return {
    queriesTs: existsSync(queriesTsPath) ? mtime(queriesTsPath) : 0,
    schemaSql: existsSync(schemaSqlPath) ? mtime(schemaSqlPath) : 0,
  };
}

function localDbExists(): boolean {
  try {
    statSync(localDbPath);
    return true;
  } catch {
    return false;
  }
}

function parseQueries(sqlContent: string): QueryBlock[] {
  const lines = sqlContent.split(/\r?\n/);
  const blocks: QueryBlock[] = [];
  const names = new Set<string>();

  let currentName: string | null = null;
  let currentStart = 1;
  let currentSqlLines: string[] = [];

  function pushCurrent(): void {
    if (!currentName) return;

    const sql = currentSqlLines.join("\n").trim();
    if (!sql) {
      throw new Error(`Query "${currentName}" at line ${currentStart} is empty`);
    }

    blocks.push({ name: currentName, sql });
    currentName = null;
    currentSqlLines = [];
  }

  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index] ?? "";
    const match = line.match(queryMarker);

    if (!match) {
      if (currentName) currentSqlLines.push(line);
      continue;
    }

    pushCurrent();

    const name = match[1] ?? "";
    if (names.has(name)) {
      throw new Error(`Duplicate query name "${name}" at line ${index + 1}`);
    }

    names.add(name);
    currentName = name;
    currentStart = index + 1;
    currentSqlLines = [];
  }

  pushCurrent();

  if (blocks.length === 0) {
    throw new Error("No queries found in sql/queries.sql");
  }

  return blocks;
}

function run(command: string, argsList: string[], options?: { input?: string }) {
  const result = spawnSync(command, argsList, {
    cwd: appDir,
    encoding: "utf8",
    input: options?.input,
    stdio: options?.input === undefined ? "pipe" : undefined,
  });

  if (result.status !== 0) {
    throw new Error(
      `${command} ${argsList.join(" ")} failed\n${result.stderr ?? result.stdout ?? ""}`.trim(),
    );
  }

  return result.stdout ?? "";
}

function resolveTypeSqlCliInvocation(): { command: string; argsPrefix: string[] } {
  const packageJsonPath = require.resolve("typesql-cli/package.json", { paths: [appDir] });
  const packageDir = dirname(packageJsonPath);
  const cliJsPath = join(packageDir, "cli.js");
  const distCliJsPath = join(packageDir, "dist", "src", "cli.js");
  const srcCliTsPath = join(packageDir, "src", "cli.ts");

  if (existsSync(cliJsPath)) {
    return { command: process.execPath, argsPrefix: [cliJsPath] };
  }
  if (existsSync(distCliJsPath)) {
    return { command: process.execPath, argsPrefix: [distCliJsPath] };
  }
  if (existsSync(srcCliTsPath)) {
    return { command: process.execPath, argsPrefix: ["--import", "tsx", srcCliTsPath] };
  }

  throw new Error(
    "Unable to locate TypeSQL CLI entrypoint. Tried cli.js, dist/src/cli.js, and src/cli.ts.",
  );
}

function rebuildLocalDb(): void {
  writeFileSync(localDbPath, "");

  for (const file of listSqlFiles(migrationsDir)) {
    run("sqlite3", [localDbPath], {
      input: readFileSync(file, "utf8"),
    });
  }
}

function writeSchemaDump(): void {
  const dump = run("sqlite3", [localDbPath, ".schema --indent"]);
  writeFileSync(schemaSqlPath, `${generatedSchemaHeader}\n\n${dump.trim()}\n`);
  console.log(`wrote ${relative(appDir, schemaSqlPath)}`);
}

function resolveMaybeRelativePath(path: string): string {
  if (path.includes("://") || isAbsolute(path)) return path;
  return join(appDir, path);
}

function loadTypeSqlConfig(): TypeSqlConfig {
  const parsed = JSON.parse(readFileSync(typesqlConfigPath, "utf8")) as Partial<TypeSqlConfig>;
  if (!parsed || typeof parsed !== "object") {
    throw new Error("typesql.json must be a JSON object");
  }
  if (!parsed.databaseUri || typeof parsed.databaseUri !== "string") {
    throw new Error("typesql.json missing required string field: databaseUri");
  }
  if (!parsed.sqlDir || typeof parsed.sqlDir !== "string") {
    throw new Error("typesql.json missing required string field: sqlDir");
  }
  if (!parsed.client || typeof parsed.client !== "string") {
    throw new Error("typesql.json missing required string field: client");
  }
  return {
    databaseUri: parsed.databaseUri,
    sqlDir: parsed.sqlDir,
    outDir: typeof parsed.outDir === "string" ? parsed.outDir : undefined,
    client: parsed.client,
    moduleExtension:
      typeof parsed.moduleExtension === "string" ? parsed.moduleExtension : undefined,
  };
}

function generateOnce(): void {
  const sqlContent = readFileSync(queriesSqlPath, "utf8");
  const blocks = parseQueries(sqlContent);
  const baseConfig = loadTypeSqlConfig();

  const tempDir = mkdtempSync(join(tmpdir(), "ingress-proxy-typesql-"));
  const tempSqlDir = join(tempDir, "sql");
  const tempOutDir = join(tempDir, "out");

  try {
    mkdirSync(tempSqlDir, { recursive: true });
    mkdirSync(tempOutDir, { recursive: true });

    writeFileSync(
      join(tempDir, "typesql.json"),
      JSON.stringify(
        {
          ...baseConfig,
          databaseUri: resolveMaybeRelativePath(baseConfig.databaseUri),
          sqlDir: tempSqlDir,
          outDir: tempOutDir,
        },
        null,
        2,
      ),
    );

    for (const block of blocks) {
      writeFileSync(join(tempSqlDir, `${block.name}.sql`), `${block.sql}\n`);
    }

    const typesqlCli = resolveTypeSqlCliInvocation();
    run(typesqlCli.command, [
      ...typesqlCli.argsPrefix,
      "compile",
      "--config",
      join(tempDir, "typesql.json"),
    ]);

    const contents = blocks.map((block, index) => {
      const outPath = join(tempOutDir, `${block.name}.ts`);
      let out = readFileSync(outPath, "utf8").trim();

      if (index > 0) {
        out = out
          .replace(/^import type \{[^}]+\} from '@cloudflare\/workers-types';\s*/m, "")
          .trim();
      }

      return out;
    });

    const combined = `${generatedHeader}\n\n${contents.join("\n\n")}\n`;
    writeFileSync(queriesTsPath, combined);
    console.log(`wrote ${relative(appDir, queriesTsPath)} from ${blocks.length} queries`);
  } finally {
    rmSync(tempDir, { recursive: true, force: true });
  }
}

function maybeGenerate(): void {
  const sourceTimes = getSourceMtTimes();
  const targetTimes = getTargetMtTimes();
  const sourceMax = Math.max(sourceTimes.queries, sourceTimes.migrations, sourceTimes.typesql);
  const targetMin = Math.min(
    targetTimes.queriesTs || Number.POSITIVE_INFINITY,
    targetTimes.schemaSql || Number.POSITIVE_INFINITY,
  );

  const stale = targetTimes.queriesTs === 0 || targetTimes.schemaSql === 0 || sourceMax > targetMin;
  const migrationsChanged =
    sourceTimes.migrations > Math.min(targetTimes.queriesTs || 0, targetTimes.schemaSql || 0);

  if (!forceMode && !stale) {
    console.log("queries.ts and schema.sql are up to date");
    return;
  }

  if (!localDbExists() || migrationsChanged || targetTimes.schemaSql === 0) {
    console.log("rebuilding .local.db from sql/migrations");
    rebuildLocalDb();
  }

  generateOnce();
  writeSchemaDump();
}

function watch(): void {
  let fingerprint = JSON.stringify({
    sourceTimes: getSourceMtTimes(),
    targetTimes: getTargetMtTimes(),
  });
  let running = false;
  let queued = false;

  const tick = () => {
    const next = JSON.stringify({
      sourceTimes: getSourceMtTimes(),
      targetTimes: getTargetMtTimes(),
    });

    if (next === fingerprint) return;
    fingerprint = next;

    if (running) {
      queued = true;
      return;
    }

    running = true;
    try {
      maybeGenerate();
    } catch (error) {
      console.error(error);
    } finally {
      running = false;
      if (queued) {
        queued = false;
        tick();
      }
    }
  };

  maybeGenerate();
  console.log(
    `watching ${relative(appDir, queriesSqlPath)}, sql/migrations/**/*.sql, typesql.json`,
  );
  setInterval(tick, 800);
}

if (watchMode) {
  watch();
} else {
  maybeGenerate();
}
