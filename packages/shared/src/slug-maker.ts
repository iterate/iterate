const REQUIRED_SLUG_PARTS = 3;

if (FUNNY_SLUG_WORDS.length !== 200) {
  throw new Error(
    `FUNNY_SLUG_WORDS must contain exactly 200 entries, got ${FUNNY_SLUG_WORDS.length}`,
  );
}

export function makeFunnySlug(): string {
  const chosen = new Set<string>();

  while (chosen.size < REQUIRED_SLUG_PARTS) {
    chosen.add(FUNNY_SLUG_WORDS[randomInt(FUNNY_SLUG_WORDS.length)]!);
  }

  return [...chosen].join("-");
}
